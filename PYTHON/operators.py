# ASSIGNMENT operators
x=10
print("value of x:",x)

y=5
y+=3 #ADD and ASSIGN
print("value of y:",y)

z=8
z-=2
print("value of z:",z)
#%%
#COMPARISION operators
x=5
y=10
result=x==y
print(result)

x=7
y=7
result=x!=y
print(result)

num1=15
num2=10
result=num1>num2
print(result)

p=4
q=8
r=p<q
print(r)
#%%
#LOGICAL operators
x=True
y=False
z=x and y
print(z)

a=True
b=False
c= a or b
print(c)

p=True
q = not p
print(q)
#%%
#MEMBERSHIP operator
t=[1,2,3,4,5]
print(10 in t)
print(7 not in t)
#%%
#BITWISE operators
left_shift=2<<2
print(left_shift)
right_shift=32>>2
print(right_shift)


















